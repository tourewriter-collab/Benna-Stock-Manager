-- AUTOMATIC TIMESTAMPS TRIGGERS & FUNCTIONS
---------------------------------------------------------

-- Create a helper function to update updated_at / sync_updated_at automatically
CREATE OR REPLACE FUNCTION update_sync_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.sync_updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Apply triggers to tables that have sync_updated_at
CREATE TRIGGER trigger_update_users_sync_updated_at BEFORE UPDATE ON users FOR EACH ROW EXECUTE FUNCTION update_sync_timestamp();
CREATE TRIGGER trigger_update_categories_sync_updated_at BEFORE UPDATE ON categories FOR EACH ROW EXECUTE FUNCTION update_sync_timestamp();
CREATE TRIGGER trigger_update_suppliers_sync_updated_at BEFORE UPDATE ON suppliers FOR EACH ROW EXECUTE FUNCTION update_sync_timestamp();
