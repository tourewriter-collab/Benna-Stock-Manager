    longitude NUMERIC(10, 7),
    last_location_update TEXT,
    sync_status TEXT DEFAULT 'synced',
    sync_updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    is_archived BOOLEAN DEFAULT FALSE,
    _sync_error TEXT
);

